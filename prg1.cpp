#include<iostream>
using namespace std;
int main()
{
	int n;
	cout << "Enter the number";
	cin >> n;
	int i=1,j=n;
	while(i<=n){

		while(j >= i){
	
			cout << i << "  ";
			j--;
		}
		j = n;
        	i++;
        	cout << endl;
	}
	
return 0;	
} 































